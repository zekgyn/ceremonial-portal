        'focus-visible:border-rose-400 border border-gray-200 rounded-xl bg-gray-50 text-gray-900 placeholder-gray-400 outline-none focus:border-rose-400 focus:bg-white transition-colors text-sm dark:bg-gray-50 dark:text-gray-900',
        props.class,
    )">
</template>
